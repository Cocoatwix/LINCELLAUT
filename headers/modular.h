
#ifndef MODULAR_H
#define MODULAR_H

/** Returns the inverse of a given number
    under some given modulus. Returns -1 if
		the inverse DNE. */
int num_inverse(int, int);

/** Returns the square root of the first given 
    number mod some modulus. Returns -1 if the
		square root DNE. */
int square_root(int, int);

#endif //MODULAR_H